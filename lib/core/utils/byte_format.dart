/// Форматирует размер в байтах в человекочитаемую строку.
///
/// Используется ДЕСЯТИЧНАЯ база (делитель 1000), чтобы число совпадало с тем,
/// что показывает системный файловый менеджер Android / проводник Windows
/// (см. issue #12: раньше делили на 1024² под подпись «МБ» — расходилось).
/// Лесенка: Б → КБ → МБ → ГБ. Один знак после запятой для КБ/МБ/ГБ.
/// Единый источник правды — все модели делегируют сюда.
String formatBytes(int bytes) {
  const k = 1000;
  if (bytes < k) return '$bytes Б';
  if (bytes < k * k) return '${(bytes / k).toStringAsFixed(1)} КБ';
  if (bytes < k * k * k) {
    return '${(bytes / (k * k)).toStringAsFixed(1)} МБ';
  }
  return '${(bytes / (k * k * k)).toStringAsFixed(1)} ГБ';
}
